Este slogan hace referencia a que en el zoológico es un lugar de biodiversidad, que en cada esquina, hay una historia que contar, hay algo que conocer y hay algo que comentar. Me parece que el slogan se enfoca en que visitar al zoológico tiene un sentido de  conocer datos curiosos, información de flora y fauna e indagación sobre especies y demás.

“ Somos la gente de la conservación ”

el slogan hace referencia al fortalecimiento del sentido de pertenencia por nuestros recursos naturales mediante a la conservación, así mismo a mostrar al valle del cauca como una región conscientemente activa en la conservación de especies y del medio ambiente. El termino conservación es un concepto que en la gente del común no esta relacionado y puede ser que no se identifiquen con el. En mi lugar, podría sugerir que en vez de conservación, colocaría somos gente de protección o somos gente de preservación.

ALTAVOCES:

En el zoológico, los altavoces son utilizados como normas de conducta en este lugar. A lo largo del recorrido, el sonido de los altavoces La parte de los altavoces la vi un poco débil, ya que solo en una zona pude escuchar la información que se trasmite a través de ellos la cual es muy importante porque son recomendaciones importantes para los visitantes acerca de sus deberes y normas dentro del zoológico y seria interesante que para evitar inconvenientes sea notorio y que exista con mayor presencia la ubicación de los altavoces.

SEÑALETICA:

El zoológico esta muy enriquecido, ya que desde la entrada cuenta con señales, además del mapa que entregan en la entrada todo con el fin de direccionar a las personas que entran al zoológico. También las señales indican cuanto hace falta para llegar a los estaderos, los restaurantes, baños,etc .

INFORMACION DE ESPECIES:

Cuenta con bastante y suficiente información basta de cada especie que se encuentra en el zoológico. Además que en algunos sectores involucran a los niños y a las familias.

ESPACIOS DE DESCANSO:

Las zonas de descanso son muy recurridas en el zoológico ya que son zonas donde se pueden tomar un break y disfrutar del ambiente ecológico por un momento en este lugar. Las zonas de descanso están armonizadas y van acorde a con el zoológico. Los colores que manejan, la comida van de la mano con el resto de los componentes de este lugar.</p>
